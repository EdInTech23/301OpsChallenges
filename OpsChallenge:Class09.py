# Dog sizes
height = 24  # inches
weight = 45  # pounds

# Check if the dog is small
if height <= 12 and weight <= 25:
    print("The dog is small.")

# Check if the dog is medium
elif height <= 18 and weight <= 50:
    print("The dog is medium.")

# Check if the dog is large
elif height <= 24 and weight <= 75:
    print("The dog is large.")

# Check if the dog is giant
else:
    print("The dog is giant.")
